/* Practica 02: 
   Calcular el promedio de 5 calificaciones


 */
package A_Secuencial;

public class Practica02 {

    public static void main(String[] args) {
        double nota1 = 2.5;
        double nota2 = 6.25;
        double nota3 = 5;
        double nota4 = 9.02;
        double nota5 = 1.25;

        double media = (nota1 + nota2 + nota3 + nota4 + nota5) / 5;

        System.out.println("La media de las calificaciones es de: " + media);
    }
}
